-- Цей скрипт НЕ ОБОВ'ЯЗКОВИЙ, якщо ви оновили код
-- Але якщо хочете щоб anon міг читати feedback - запустіть це
-- ============================================

-- ВАРІАНТ 1: Дозволити anon читати всі записи (якщо це публічні дані)
-- Видаліть стару політику
DROP POLICY IF EXISTS "Allow read for authenticated users" ON feedback;

-- Створіть нову політику що дозволяє читання всім
CREATE POLICY "Allow read for all users" ON feedback
  FOR SELECT
  USING (true);

-- ============================================

-- ВАРІАНТ 2: Залишити як є (рекомендовано)
-- Код вже виправлено щоб не використовувати .select()
-- Це безпечніше - anon може тільки INSERT, але не SELECT

-- ============================================
-- ПЕРЕВІРКА
-- ============================================
-- Перевірте поточні політики:
SELECT 
    policyname,
    cmd AS operation,
    roles,
    qual AS using_check
FROM pg_policies
WHERE schemaname = 'public' AND tablename = 'feedback'
ORDER BY cmd;
